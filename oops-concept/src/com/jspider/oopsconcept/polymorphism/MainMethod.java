package com.jspider.oopsconcept.polymorphism;

public class MainMethod {
public static void main(int a) {
	System.out.println("this is main method with int argument");
}
public static void main(int a,double b) {
	System.out.println("this is main method from int ,int argument");
}
public static void main(String[]args) {
	System.out.println("this is main method from string array argument");
}
}
